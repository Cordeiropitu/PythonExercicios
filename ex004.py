var = input('Digite algo: ')
print(
    'O tipo primitivo desse valor é: ', type(var),
    'É número? ', var.isnumeric(),
    'É alfanumérico? ', var.isalnum(),
    'Está em minúsculo? ',var.islower(),
    'Está em maiúsculo? ', var.isupper(),
    'Só tem espaços? ', var.isspace()
)

